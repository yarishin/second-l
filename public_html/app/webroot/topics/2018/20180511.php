<tr><td colspan="2"><br />（３）異動の目的</td></tr>
<tr><td colspan="2">
総合金融コンサルタント事業者としての当社の内部統制の整備及び運用状況に関する内部監査機能をより一層強化すべく、今般、古谷氏を内部監査部門の責任者として、当社取締役に迎え入れることと致しました。これにより、内部統制の実効性を一層向上させ、投資家の皆様が安心してお取引いただける企業となるよう、尽力する所存です。
また、後記「取締役会及び監査役設置会社への移行」に伴い、これまで当社取締役であった園田氏が監査役へ就任いたしました。


<br /><br />
<tr><td colspan="2">	<p>２．取締役会及び監査役設置会社への移行</p></td></tr>
<tr><td colspan="2">（１）移行の理由</td></tr>
<tr><td colspan="2">

当社は、本業である総合金融コンサルタント事業者として長期的に企業価値を最大化するためには、内部統制だけでなく、不正行為を防止し、生産性を向上させ、あらゆるステークホルダーに対して企業の社会的責任を全うすることが重要であると考えています。
こうした企業経営の根幹を持続させるべく、この度、業務執行の意思決定等を行う取締役会と当該業務執行を監査する監査役の設置を行うことといたしました。これにより商法上の原則である「所有と経営の分離」を実現し、コーポレート・ガバナンスを強化してまいります。



