<br />
<br />
<strong>1.出店理由</strong><br /><br />　現在の沖縄県では那覇空港で進行中の第２滑走路埋立工事を始め、米軍普天間飛行場（沖縄県宜野湾市）の県内移設に向けた辺野古の新基地建設等の公共工事の着工が既に始まっています。また、辺野古で予定されている埋立面積は概ね160ヘクタールで、工費は3,500～5,000億円と想定されています。更に、浦添市が受け入れを表明した軍港跡地の浦添移設に伴う再開発が辺野古の埋立面積の2倍近い300ヘクタールの埋立てで、かつ工費は8,000億円超と推計される等、今後の沖縄県では10年～15年程度に渡る大規模公共事業が予定されているため。<br />

<br />


<strong>2.なぜ銀行ではなくトラストレンディングなのか</strong><br /><br />
　大規模公共事業の元受会社はスーパーゼネコンであり、末端の下請け会社は、ほぼ地元の中小建設会社及び中規模ゼネコンとのJVでの事業参入のため、銀行融資を受けるための実績要件を満たせないことから、大型重機等のリースに係る融資審査が通らない状況です。そのため、銀行融資を受けるための約１年程度の実績を残す必要があります。
<br />　そうした中、当社の上級顧問である豊岡は九州財務局局長及び内閣府沖縄復興局における経歴と地元企業との人的交流があり、かつ昨年就任した２名の財務省出身の取締役も沖縄振興開発金融公庫への出向経験があることから、それらの人的交流を通じて沖縄地方に本社を置く公共事業受注団の関連会社にトラストレンディングで融資を行うことで、地域貢献の一助を担っていく所存です。
<br /><br />


<strong>3.沖縄支店の実務</strong><br /><br />
　この度の沖縄支店開設に際しては、適宜東京本社との連携を図りつつ、融資先により近い場所に拠点を設けることで、貸付先の与信管理事業及び債権の管理・回収事業において、地域に密着したきめ細かいサービスを提供し、投資家様の資産を安全に管理できるものと考えております。
